# set vs tuples
""""
Sets are unordered collections of unique elements.
They are defined using curly braces {} and can also be created using the set() constructor.
Sets do not allow duplicate elements. If you try to add a duplicate element, it will be ignored.
"""

"""""
Tuples are ordered collections of elements.
They are defined using parentheses () and can also be created without parentheses.
Tuples are immutable, meaning once they are created, their elements cannot be changed.
"""
thisset = {"apple", "banana", "cherry"}
print(thisset)